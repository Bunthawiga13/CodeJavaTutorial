public class WhileLoop {
    public static void main(String[] args) {
        for(int i = 1; i <= 100; i++){
            System.out.println("result in forLoop : "+i);
        }

        int iwhile = 1;
        while (iwhile <= 5) {
            System.out.println("Hello");
            iwhile++ ;
            
        }
    }
}
